def get_color(val, limit):
    if limit is None: return "#1a5b3c"
    if val > limit:
        return "#c1121f"
    elif val > (limit * 0.95):
        return "#e47911"
    else:
        return "#1a5b3c"
def get_cg_color(cg, limits):
    if not limits: return "#1a5b3c"
    mn, mx = limits
    margin = (mx - mn) * 0.05
    if cg < mn or cg > mx:
        return "#c1121f"
    elif cg < mn + margin or cg > mx - margin:
        return "#e47911"
    else:
        return "#1a5b3c"
